
# Strings, integers and floats
s = "abc"
i = 1
f = 0.123

print(s)
print(i)
print(f)

# Lists
a = [1, "123", 3.412]

print(a[0])

print(a[-1])

print(a[0:1])
print(a[0:2])

print(a*3)

# Indexing
s = "123"

print(s[0:-1])

# Nested lists
l = [['123',2,3], [3,4,5], [5,6,7], [8,9,'111']]

print(l[0:3])
print(l[0])
print(l[0][0])
print(l[0][0][0])

# Dictionaries
d = {"cup": "tea", "glass": "water", "bowl": "soup"}

print(d["cup"])

print(d.keys())

d["plate"] = "dinner"

print(d)
